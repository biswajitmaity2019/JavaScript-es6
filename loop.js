let i=0;
//While loop
while(i<5)
{
    console.log("The number is "+i);
    i++;
}
//do-while loop
do{
    console.log("The number is "+i);
    i++;
}while(i<5);
//for loop
for (i=0;i<5;i++)
{
    console.log("The number is "+i);
}
//switch case statement
let x=prompt("Enter value from 1 to 5",);
switch(x)
{
            case '1':
            console.log("Enter number is "+x);
            break;
            case '2':
            console.log("Enter number is "+x);
            break;
            case '3':
            console.log("Enter number is "+x);
            break;
            case '4':
            console.log("Enter number is "+x);
            break;
            case '5':
            console.log("Enter number is "+x);
            break;
            default:
            console.log("Enter wrong number is "+x);
            break;
}
